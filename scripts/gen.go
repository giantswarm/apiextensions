func generateDeepcopy() error {
	genericArgs, customArgs := deepcopyargs.NewDefaults()
	genericArgs.GoHeaderFilePath = filepath.Join(args.DefaultSourceTree(), util.BoilerplatePath())
	genericArgs.AddFlags(pflag.CommandLine)
	customArgs.AddFlags(pflag.CommandLine)
	flag.Set("logtostderr", "true")
	pflag.CommandLine.AddGoFlagSet(flag.CommandLine)
	pflag.Parse()

	if err := deepcopyargs.Validate(genericArgs); err != nil {
		klog.Fatalf("Error: %v", err)
	}

	b, err := genericArgs.NewBuilder()
	if err != nil {
		return err
	}

	c, err := generator.NewContext(b, deepcopygen.NameSystems(), deepcopygen.DefaultNameSystem())
	if err != nil {
		return err
	}

	packages := deepcopygen.Packages(c, genericArgs)
	if err := c.ExecutePackages(genericArgs.OutputBase, packages); err != nil {
		return err
	}
	return nil
}

func generateCRDs() {
	crds := []*v1beta1.CustomResourceDefinition{
		v1alpha1.NewReleaseCRD(),
	}
	for _, crd := range crds {
		filename := fmt.Sprintf("%s_%s.yaml", crd.Spec.Group, crd.Spec.Names.Singular)
		encoded, err := yaml.Marshal(crd)
		if err != nil {
			log.Fatal(err)
		}
		err = ioutil.WriteFile(filename, encoded, 0644)
		if err != nil {
			log.Fatal(err)
		}
	}
}

func listGroupVersions() ([]types.GroupVersions, error) {
	var groupVersions []types.GroupVersions
	groupVersionVersions := map[string][]types.PackageVersion{}
	err := filepath.Walk("pkg/apis", func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if !info.IsDir() {
			return nil
		}
		split := strings.Split(path, string(filepath.Separator))
		if len(split) != 4 {
			return nil
		}
		group := split[2]
		version := split[3]
		versionGroupVersions, ok := groupVersionVersions[group]
		if !ok {
			groupVersions = append(groupVersions, types.GroupVersions{
				PackageName: group,
				Group:       types.Group(group),
			})
		}
		groupVersionVersions[group] = append(versionGroupVersions, types.PackageVersion{
			Version: types.Version(version),
			Package: fmt.Sprintf("github.com/giantswarm/apiextensions/pkg/apis/%s/%s", group, version),
		})
		return nil
	})
	if err != nil {
		return nil, err
	}
	for i, group := range groupVersions {
		groupVersions[i].Versions = groupVersionVersions[string(group.Group)]
	}
	return groupVersions, nil
}

func generateClients() {
	groupVersions, err := listGroupVersions()
	if err != nil {
		log.Fatal(err)
	}

	customArgs := clientargs.CustomArgs{
		Groups:                 groupVersions,
		IncludedTypesOverrides: nil,
		ClientsetName:          "versioned",
		ClientsetAPIPath:       "/apis",
		ClientsetOnly:          false,
		FakeClient:             true,
	}

	var inputDirs []string
	for _, group := range groupVersions {
		for _, version := range group.Versions {
			inputDirs = append(inputDirs, version.Package)
		}
	}

	genericArgs := args.GeneratorArgs{
		InputDirs:                  inputDirs,
		OutputBase:                 "./pkg/generated",
		OutputPackagePath:          "github.com/giantswarm/apiextensions/pkg/clientset",
		OutputFileBaseName:         "",
		GoHeaderFilePath:           "./scripts/boilerplate.go.txt",
		GeneratedByCommentTemplate: "// Code generated by client-gen. DO NOT EDIT.",
		VerifyOnly:                 false,
		IncludeTestFiles:           false,
		GeneratedBuildTag:          "",
		CustomArgs:                 &customArgs,
	}

	b, err := genericArgs.NewBuilder()
	if err != nil {
		log.Fatal(err)
	}

	c, err := generator.NewContext(b, clientgen.NameSystems(), clientgen.DefaultNameSystem())
	if err != nil {
		log.Fatal(err)
	}

	packages := clientgen.Packages(c, &genericArgs)
	if err := c.ExecutePackages(genericArgs.OutputBase, packages); err != nil {
		log.Fatal(err)
	}

	if err := os.RemoveAll("./pkg/clientset"); err != nil {
		log.Fatal(err)
	}

	if err := os.Rename(
		"./pkg/generated/github.com/giantswarm/apiextensions/pkg/clientset",
		"./pkg/clientset",
	); err != nil {
		log.Fatal(err)
	}

	if err := os.RemoveAll("./pkg/generated"); err != nil {
		log.Fatal(err)
	}
}

func main() {
	generateDeepcopy()
	generateClients()
	generateCRDs()
}